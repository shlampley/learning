        # import math functions
from math import *

        # printing numbers
print(90, -23, 2.067)

        #basic math
print(10 + 10)
print(10 - 10)
print(10 / 10)
print(10 * 10)
print(10 * 10 + 10)
        # order of operation
print(10 * (10 + 10))
        # modulus "10/3=3 R=1"
print(10 % 3)
        # store into variables
my_num = 10
print(my_num)
print(my_num + my_num)
        # convert number into string
print(str(my_num))
        # use str function so number can be printed next to text
            ##### print(my_num + " my favorite number") ##### will fail #####
print(str(my_num) + " my favorite number")

        # common functions
my_num = 10
        # absolute value
print(abs(my_num))
        # power "raise a 'number' to the power of another 'number' "
print(pow(my_num, 2))
        # prints max of "numbers" passed to it
print(max(my_num, 7, 34))
        # prints min of "numbers" passed to it
print(min(my_num, 7, 34))
        # round a number + or -
print(round(8.764723864846))
    # floor "round down"
print(floor(6.78))
        # ceil "round up"
print(ceil(6.78))
        # sqrt "square root of number"
print(sqrt(6.78))
